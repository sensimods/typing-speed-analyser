const handleInputAreaEvent = e => {
  switch (e.type) {
    case 'keydown':
      if (e.key === 'Backspace') {
        e.preventDefault()
      }

      let placeholderWordCount = 0
      let inputWordCount = 0

      const placeholderLines = inputAreaPlaceholder.value.split('\n')
      for (let i = 0; i < placeholderLines.length; i++) {
        const placeholderWords = placeholderLines[i].split(' ')
        placeholderWordCount = placeholderWords.length
      }

      if (e.code === 'Space' && !spacePressed) {
        const inputLines = inputArea.value.split('\n')
        for (let i = 0; i < inputLines.length; i++) {
          const inputWords = inputLines[i].trim().split(' ')
          inputWordCount = inputWords.length
        }

        spacePressed = true

        if (inputWordCount == placeholderWordCount) {
          e.preventDefault()
          inputArea.value += '\n'
        }
      } else if (e.code === 'Space' && spacePressed) {
        e.preventDefault()
      }
      break

    case 'keyup':
      if (e.code === 'Space') {
        spacePressed = false
      }
      break

    case 'scroll':
      inputArea.scrollTo({ top: textareaScrollY, behavior: 'auto' })
      inputAreaPlaceholder.scrollTop = inputArea.scrollTop
      textareaScrollY += 105
      break

    case 'focus':
      inputArea.value = ''
      break

    case 'input':
      if (!timerRunning) {
        runTimer()
      }

      if (e.data !== null) {
        totalChars++
      }

      const char = inputArea.value
      const inputLength = char.length
      const lastInputChar = char.charAt(inputLength - 1)
      const placeholderValue = inputAreaPlaceholder.value
      const lastPlaceholderChar = placeholderValue.charAt(inputLength - 1)

      if (lastInputChar !== lastPlaceholderChar) {
        inputArea.value = placeholderValue.substring(0, inputLength)
        incorrectChars++
        showError()
      }
      break

    default:
      return false
  }
}

timeOptions.forEach(option => {
  option.addEventListener('click', function () {
    timeOptions.forEach(op => {
      op.classList.remove('active-time-option')
    })
    this.classList.add('active-time-option')
    timerEl.innerText = `${this.innerText}s`
    timer = +this.innerText
    timeChosen = +this.innerText
    inputArea.value = ''
    genrateText()
    inputArea.focus()
  })
})

// global functions
const runTimer = () => {
  timerRunning = true

  const timerInterval = setInterval(() => {
    timer--
    if (timer < 0) {
      timerEl.innerText = `Time up!`
      clearInterval(timerInterval)
      reset()
    } else {
      timerEl.innerText = `${timer}s`
    }
  }, 1000)
}

const reset = () => {
  timerRunning = false
  timer = timeChosen || 15
  inputArea.blur()
  evaluateResult()
  incorrectChars = 0
  totalChars = 0
}

const evaluateResult = () => {
  const accuracy = (1 - incorrectChars / totalChars) * 100
  accuracyEl.innerText = `Accuracy: ${accuracy.toFixed(1)}%`
  let wpm = 0
  switch (timeChosen) {
    case 15:
      wpm = (totalChars / awl) * 4
      break
    case 30:
      wpm = (totalChars / awl) * 2
      break
    case 60:
      wpm = totalChars / awl
      break
    case 120:
      wpm = totalChars / awl / 2
      break
    default:
      wmp = NaN
      break
  }

  arrangeWpmScores(wpm)
  wpmEl.innerHTML = `<span title='Words Per Minute'>WPM:</span> ${wpm}`
  showResultsModal(wpm, accuracy)
}

const showResultsModal = (wpm, accuracy) => {
  resultsModal.classList.add('show')
  if (totalWpmScores.length > 1) {
    resultsModalFooter.classList.add('show')
    averageWpmEl.innerText = `Average: ${averageWpm().toFixed(2)}`
    bestWpmEl.innerText = `Best: ${bestWpmScore()}`
    typingLevelEl.innerText = `Rank: ${typingLevel()}`
  }
  setTimeout(() => {
    accuracyGauge.refresh(accuracy)
    wpmGauge.refresh(wpm)
  }, 500)
}

const averageWpm = () => {
  let sum = 0
  totalWpmScores.forEach(score => {
    sum += score
  })

  return sum / totalWpmScores.length
}

const arrangeWpmScores = wpm => {
  totalWpmScores.push(wpm)
  totalWpmScores.sort((a, b) => a - b)
}

const bestWpmScore = () => {
  const bestScore = totalWpmScores[totalWpmScores.length - 1]
  return bestScore.toFixed(2)
}

const typingLevel = () => {
  if (averageWpm() < 40) {
    return 'Novice'
  }

  if (averageWpm() < 60) {
    return 'Intermediate'
  }

  return 'Advanced'
}

const showError = () => {
  inputArea.classList.add('error')
  setTimeout(() => {
    inputArea.classList.remove('error')
  }, 150)
}
