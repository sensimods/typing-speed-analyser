function genrateText() {
  for (let i = words.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[words[i], words[j]] = [words[j], words[i]]
  }

  let text = ''
  for (let i = 1; i < 5 * timer; i++) {
    if (i % 5 === 0) {
      text += words[i].toLowerCase() + '\n'
    } else {
      text += words[i].toLowerCase() + ' '
    }
  }
  inputAreaPlaceholder.value = text
}
